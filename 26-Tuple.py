# Tuple is a collection which 
# is ordered and unchangeable. Allows duplicate members.


tupple = ("xeeshan", "rooshiii", "imran")
print(tupple)


print()
# type of tupple
tupple = ("xeeshan", "rooshiii", "imran")
print(type(tupple))



print()
# length of tupple
tupple = ("xeeshan", "rooshiii", "imran")
print(len(tupple))


print()
# Create Tuple With One Item  you have to add a comma after the item, 
# otherwise Python will not recognize it as a tuple.
tupple = ("xeeshan",)
print(type(tupple))



print()
# A tuple with strings, integers and boolean values:
tupple = ("xeeshan", True, 7, "imran")
print(tupple)


print()
# use the tuple() constructor to make a tuple.
thistuple  = tuple(("xeeshan", True, 7, "imran"))
print(thistuple)